      },
      child: MaterialApp(
        title: 'Mental Health Tracker',
        theme: ThemeData(
          fontFamily: 'CrimsonPro',
          fontFamilyFallback: const ['Lora', 'CrimsonPro', 'CrimsonText'],
          useMaterial3: true,
          colorScheme: ColorScheme.fromSwatch(
            primarySwatch: Colors.deepPurple,
